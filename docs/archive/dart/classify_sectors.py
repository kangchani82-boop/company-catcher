def classify_by_biz(biz_content: str) -> str:
    """biz_content 텍스트 → 섹터명 (매칭 없으면 '기타').

    스코어링 방식:
    - 앞 500자(사업개요) 키워드 매칭 가중치 3x
    - 500~2000자(핵심 본문) 키워드 매칭 가중치 2x
    - 앞 5000자 전체 키워드 매칭 가중치 1x
    목차형 biz_content 대응: 500자 이후부터도 탐색.
    동점 시 BIZ_SECTORS 순서 우선.
    """
    if not biz_content:
        return "기타"
    head = biz_content[:500]      # 사업 개요 앞부분 (목차일 수 있음)
    body = biz_content[500:2000]  # 핵심 본문 (목차 뒤 실제 사업 설명)
    text = biz_content[:5000]     # 전체 5000자

    best_sector = "기타"
    best_score = 0
    for sector, keywords in BIZ_SECTORS:
        score = (sum(1 for kw in keywords if kw in head) * 3
                 + sum(1 for kw in keywords if kw in body) * 2
                 + sum(1 for kw in keywords if kw in text))
        if score > best_score:
            best_score = score
            best_sector = sector
    return best_sector


def run():
    db = sqlite3.connect(DB_PATH)
    db.row_factory = sqlite3.Row

    # 1. supply_chain의 모든 reporting 기업 (corp_code + corp_name)
    sc_corps = db.execute(
        "SELECT DISTINCT corp_code, corp_name FROM supply_chain"
    ).fetchall()
    print(f"supply_chain 기업 수: {len(sc_corps)}")

    # 2. reports 테이블에서 biz_content (기업당 가장 긴 것 사용 — 분기보고서는 목차만 있는 경우가 많음)
    biz_map: dict[str, str] = {}  # corp_code → biz_content (longest)
    rows = db.execute(
        """SELECT corp_code, biz_content
           FROM reports
           WHERE biz_content IS NOT NULL AND LENGTH(biz_content) > 200
           ORDER BY LENGTH(biz_content) DESC"""
    ).fetchall()
    for r in rows:
        cc = r["corp_code"]
        if cc not in biz_map:
            biz_map[cc] = r["biz_content"]
    print(f"biz_content 보유 기업 수: {len(biz_map)}")

    # 3. 분류 실행 (수동 오버라이드 → biz_content 키워드 순)
    results = []
    sector_cnt: dict[str, int] = {}
    for row in sc_corps:
        cc   = row["corp_code"]
        name = row["corp_name"]

        # 수동 오버라이드 먼저 확인 (정확 일치 or 기업명 내 포함)
        sector = None
        for key, sec in MANUAL_SECTOR.items():
            if name == key or key in name:
                sector = sec
                break

        if sector is None:
            biz    = biz_map.get(cc, "")
            sector = classify_by_biz(biz)

        sector_cnt[sector] = sector_cnt.get(sector, 0) + 1
        results.append((cc, name, sector))

    total = len(results)
    non_etc = sum(v for k, v in sector_cnt.items() if k != "기타")
    print(f"\n=== 분류 결과 ({total}개 기업) ===")
    for sec, cnt in sorted(sector_cnt.items(), key=lambda x: -x[1]):
        pct = cnt / total * 100
        print(f"  {sec:15s}: {cnt:4d} ({pct:5.1f}%)")
    print(f"\n  기타 제외 분류율: {non_etc}/{total} = {non_etc/total*100:.1f}%")

    # 4. companies 테이블에 저장 (UPSERT)
    now = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    db.execute("DELETE FROM companies")  # 초기화 후 재삽입
    db.executemany(
        """INSERT OR REPLACE INTO companies (corp_code, corp_name, sector, updated_at)
           VALUES (?, ?, ?, ?)""",
        [(cc, name, sector, now) for cc, name, sector in results]
    )
    db.commit()
    print(f"\n✅ companies 테이블에 {len(results)}건 저장 완료")
    db.close()


if __name__ == "__main__":
    run()
