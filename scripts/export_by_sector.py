def clean_name(name: str) -> str:
    name = name or ""
    name = re.sub(r'\(주\)|\(유\)|\(사\)', '', name)
    name = re.sub(r'주식회사|유한회사|합자회사', '', name)
    return name.strip()


def classify(corp_name: str) -> dict:
    cleaned = clean_name(corp_name)
    for rule in SECTOR_RULES:
        excludes = rule.get("exclude", [])
        if any(ex in cleaned for ex in excludes):
            continue
        if any(kw in cleaned for kw in rule["keywords"]):
            return rule
    return SECTOR_ETC


def safe_filename(s: str) -> str:
    return re.sub(r'[\\/*?:"<>|]', '_', s).strip()


