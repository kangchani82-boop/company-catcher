def lookup(induty_code):
    """code → (l1, l2, l3, name)"""
    if not induty_code: return ('', '', '', '')
    code = induty_code.strip()
    if code in KSIC_MAP:
        return KSIC_MAP[code]
    # 점진적 단축 매칭
    for k in [code[:5], code[:4], code[:3], code[:2]]:
        if k in KSIC_MAP:
            return KSIC_MAP[k]
    return ('', '', '', '')


def name_chain(induty_code):
    """code → 5단계 명칭 체인"""
    l1, l2, l3, name = lookup(induty_code)
    return {
        'l1_code': l1, 'l1_name': KSIC_L1.get(l1, ''),
        'l2_code': l2, 'l2_name': KSIC_MAP.get(l2, ('','','',''))[3] if l2 else '',
        'l3_code': l3, 'l3_name': KSIC_MAP.get(l3, ('','','',''))[3] if l3 else '',
        'leaf_code': induty_code, 'leaf_name': name,
    }
