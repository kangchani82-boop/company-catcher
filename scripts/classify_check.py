def clean_name(n):
    n = re.sub(r'\(주\)|\(유\)|\(사\)', '', n)
    n = re.sub(r'주식회사|유한회사|합자회사', '', n)
    return n.strip()

def classify_all(cn):
    cleaned = clean_name(cn)
    for sector, kws in ALL_KWS:
        if any(kw in cleaned for kw in kws):
            return sector
    return 'etc'

